#include <iostream>
using namespace std ;
int main(){
    int a[3][3];
    int b[3][3];
    for(int i=0 ; i<3 ; i++){
        for(int j=0 ; j<3 ; j++){
            cin >> a[i][j];
        }
    }
    if((a[0][0]+a[0][1]+a[1][0])%2==1)b[0][0]=0;
    else b[0][0]=1;
    
    if((a[0][1]+a[0][0]+a[0][2]+a[1][1])%2==1)b[0][1]=0;
    else b[0][1]=1;
    
    if((a[0][2]+a[0][1]+a[1][2])%2==1)b[0][2]=0;
    else b[0][2]=1;
    
    if((a[1][0]+a[0][0]+a[2][0]+a[1][1])%2==1)b[1][0]=0;
    else b[1][0]=1;
    
    if((a[1][1]+a[0][1]+a[1][0]+a[1][2]+a[2][1])%2==1)b[1][1]=0;
    else b[1][1]=1;
    
    if((a[1][2]+a[1][1]+a[0][2]+a[2][2])%2==1)b[1][2]=0;
    else b[1][2]=1;
    
    if((a[2][0]+a[1][0]+a[2][1])%2==1)b[2][0]=0;
    else b[2][0]=1;
    
    if((a[2][1]+a[2][0]+a[1][1]+a[2][2])%2==1)b[2][1]=0;
    else b[2][1]=1;
    
    if((a[2][2]+a[2][1]+a[1][2])%2==1)b[2][2]=0;
    else b[2][2]=1;
    
    for(int i=0 ; i<3 ; i++){
        for(int j=0 ; j<3 ; j++){
            cout << b[i][j];
        }
        cout <<"\n";
    }
    return 0;
    
}